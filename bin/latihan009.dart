void main() {
  // operator perbandingan
  // Operator perbandingan adalah operator yang hasilnya adalah bool
  // Operator perbandingan bisa digunakan di tipe data number dan string
  // == adalah sama dengan
  // != adalah tidak sama dengan
  // > adalah lebih dari
  // < adalah kurang dari
  // >= adalah lebih dari atau sama dengan
  // <= adalah kurang dari atau sama dengan
  var hasil1 = 'kharis' == 'Kharis';
  var hasil2 = 20 != 15;
  var hasil3 = 20 > 15;
  var hasil4 = 20 < 15;
  var hasil5 = 20 >= 20;
  var hasil6 = 20 <= 15;
  print(hasil1);
  print(hasil2);
  print(hasil3);
  print(hasil4);
  print(hasil5);
  print(hasil6);
}
